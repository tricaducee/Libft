#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	//if (dstsize < 1)
	//	return (ft_strlen(src));
	while (dst[i])
		i++;
	if (i >= dstsize)
		return (dstsize + ft_strlen(src));
	while (src[j] && i < dstsize - 1)
		dst[i++] = src[j++];
	dst[i] = 0;
	while (src[j++])
		i++;
	return (i);
}
