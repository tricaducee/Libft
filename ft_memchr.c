#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;

	i = 0;
	if (!n)
		return (0);
	while (i < n)
	{
		if (((char *)s)[i] == (char)c)
			return ((void *)s + i);
		i++;
	}
	if (c == 0)
		return ((void *)s);
	return (0);
}
